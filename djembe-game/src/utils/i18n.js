let currentLang = 'ko';

export const i18n = {
  setLang(lang) { if (DICT[lang]) currentLang = lang; },
  getLang() { return currentLang; },
  t(key, ...args) {
    let str = (DICT[currentLang] && DICT[currentLang][key]) || (DICT.ko && DICT.ko[key]) || key;
    args.forEach(a => { str = str.replace('%s', a); });
    return str;
  }
};
